int main() {
    int a, b;

    int suma;
    int resta;
    int multiplicacion;
    int division;
    
    a = 15;
    b = 5;

    suma = a + b;
    resta = a - b;
    multiplicacion = a * b;
    division = a / b;

    printf("Suma: %d\n", suma);
    printf("Resta: %d\n", resta);
    printf("Multiplicación: %d\n", multiplicacion);
    printf("División: %d\n", division);

    return 0;
}
