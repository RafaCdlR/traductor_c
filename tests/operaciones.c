int main() {
    int a, b;
    a = 15;
    b = 5;

    int suma;
    suma = a + b;
    
    int resta;
    resta = a - b;
    
    int multiplicacion;
    multiplicacion = a * b;
    
    int division;
    division = a / b;

    printf("Suma: %d\n", suma);
    printf("Resta: %d\n", resta);
    printf("Multiplicación: %d\n", multiplicacion);
    printf("División: %d\n", division);

    return 0;
}
